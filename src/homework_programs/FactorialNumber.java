public class FactorialNumber 
{
public static void main(String[] args)
{
	  // Factorial Number of 3=  3*2*1
	
	int factorial=1;  //
	for (int i=1;i<=3;i++) // 3
	{
		factorial=factorial*i; //1*1=1*2*3
	}
	
	System.out.println(factorial);
	
	
System.out.println("*****************************************************");


      // factorial Number of 6

    factorial=1;
    for (int i=1;i<=6;i++)
    {
    	factorial=factorial*i;
    }
    
    System.out.println(factorial);



	
}
}
