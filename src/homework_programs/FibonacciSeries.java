package homework_programs;

public class FibonacciSeries
{
	public static void main(String[] args)
	{
		
	
	int n1=1,n2=2,n3;  //
	
	System.out.println(n1);
	System.out.println(n2);
	
	for (int a=1;a<=4;a++) //a=2
	{
		n3=n1+n2;  //3
		System.out.println(n3);
		
		
		n1=n2;  //n1=3
		n2=n3;  //n2=5
		
	}
		
	
	}
}

 