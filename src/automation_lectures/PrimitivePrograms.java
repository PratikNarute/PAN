package automation_lectures;

public class PrimitivePrograms 
{
public static void main(String[] args)
{
	
byte f=100;  //byte examples
System.out.println(+f);

short g=30000;  //short example
System.out.println(+g);


int a=40,b=100,c=80,d=10; //int examples
int e=a+b-c+d;
System.out.println(+e);


long h=500,i=5000;  //long examples
long j=h*i;
System.out.println(+j);


float x=4/7f,y=2/8f;  //float examples
float z=x*y; 
System.out.println(+z);


double p=10.55d,q=17.88d;  //double examples
double r=(q/p)+z;
System.out.println(+r);	


char pratik = 'I';  //character examples
System.out.println(pratik);

String PN = "I want to correct my life in better ways and I do.";  //string examples
System.out.println(PN);








}	
}
