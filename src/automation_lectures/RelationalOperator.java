package automation_lectures;

public class RelationalOperator 
{
public static void main(String[] args) 
{
int a=100,b=200;
boolean ans;
    // greater than [>]
 ans = a>b;
System.out.println(ans);


   //less than [<]
ans=a<b;
System.out.println(ans);


   // equal to [==]
ans= a==b;
System.out.println(ans);
		


   // grater than or equal to [a>=]
ans= a>=b;
System.out.println(ans);


   // less than or equal to [<=]
ans= a<=b;
System.out.println(ans);
		
		
	// not equal to [!=]
ans= a!=b;
System.out.println(ans);
		
		
	}


}
